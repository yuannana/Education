function [shape,dr,ds]=dshape(r,s)
% 形函数N
 shape(1)=(1/4)*(1-r)*(1-s);
 shape(2)=(1/4)*(1+r)*(1-s);
 shape(3)=(1/4)*(1+r)*(1+s);
 shape(4)=(1/4)*(1-r)*(1+s);
%求偏导dN
 dr(1)=-(1/4)*(1-s);
 dr(2)=(1/4)*(1-s);
 dr(3)=(1/4)*(1+s);
 dr(4)=-(1/4)*(1+s);

 ds(1)=-(1/4)*(1-r);
 ds(2)=-(1/4)*(1+r);
 ds(3)=(1/4)*(1+r);
 ds(4)=(1/4)*(1-r);
