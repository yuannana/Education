function [sx,sy,sz,sxy,syz,szx]=Solve_stress(element_solid,node,E,poisson,d)
eles_num=size(element_solid,1);
for b=1:eles_num
    x1=node(element_solid(b,2),2);
    y1=node(element_solid(b,2),3);
    z1=node(element_solid(b,2),4);
    x2=node(element_solid(b,3),2);
    y2=node(element_solid(b,3),3);
    z2=node(element_solid(b,3),4);
    x3=node(element_solid(b,4),2);
    y3=node(element_solid(b,4),3);
    z3=node(element_solid(b,4),4);
    x4=node(element_solid(b,5),2);
    y4=node(element_solid(b,5),3);
    z4=node(element_solid(b,5),4);
    x5=node(element_solid(b,6),2);
    y5=node(element_solid(b,6),3);
    z5=node(element_solid(b,6),4);
    x6=node(element_solid(b,7),2);
    y6=node(element_solid(b,7),3);
    z6=node(element_solid(b,7),4);
    x7=node(element_solid(b,8),2);
    y7=node(element_solid(b,8),3);
    z7=node(element_solid(b,8),4);
    x8=node(element_solid(b,9),2);
    y8=node(element_solid(b,9),3);
    z8=node(element_solid(b,9),4);
    stress=Hexahedral3D8Node_Stress2(E,poisson,b,d,element_solid,x1,y1,z1,x2,y2,z2,x3,y3,z3,x4,y4,z4,x5,y5,z5,x6,y6,z6,x7,y7,z7,x8,y8,z8);
    sx(b,1)=stress(1);
    sy(b,1)=stress(2);
    sz(b,1)=stress(3);
    sxy(b,1)=stress(4);
    syz(b,1)=stress(5);
    szx(b,1)=stress(6);
end